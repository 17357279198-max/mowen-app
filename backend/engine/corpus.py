def _expand_question(question: str) -> str:
    """
    扩展用户提问：将日常用语替换为语料库主题标签。
    例如："女朋友跟我吵架了" → "感情 关系 矛盾 沟通 争吵"
    这样扩展后的文本可以与语料库的 themes 更好地匹配。
    """
    if not question:
        return ""

    expanded = question

    # 检查同义词映射，将匹配的日常用语追加对应的主题标签
    for keyword, themes in SYNONYM_MAP.items():
        if keyword in question:
            expanded += " " + " ".join(themes)

    return expanded



def _tokenize(text: str) -> set:
    """
    简单分词：提取2-4字的词组，用于更细粒度的匹配。
    """
    tokens = set()
    if not text:
        return tokens

    # 去除常见停用词
    stop_chars = "的了我在和跟与是就都也还很非常不没有会要把被让给向从到在对于关于因为所以如果虽然但是而且然后不过而且着过地得"
    cleaned = "".join(c for c in text if c not in stop_chars and c not in "，。？！、；：""''（）【】《》")

    # 2字滑动窗口
    for i in range(len(cleaned) - 1):
        tokens.add(cleaned[i:i + 2])

    # 3字滑动窗口
    for i in range(len(cleaned) - 2):
        tokens.add(cleaned[i:i + 3])

    return tokens



