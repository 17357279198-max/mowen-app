def get_scenes_by_category() -> dict:
    """
    按分类分组返回所有场景。
    返回格式：{ "职场": [scene, ...], "感情": [...], ... }
    """
    result = {cat: [] for cat in SCENE_CATEGORIES}
    for scene in SCENES:
        cat = scene["category"]
        if cat in result:
            result[cat].append(scene)
    return result


def get_scene_override(scene_id: str, role_key: str) -> dict:
    """
    获取特定场景下特定角色的定制化回答。
    若不存在覆盖数据，返回 None。
    """
    scene_data = scene_overrides.get(scene_id)
    if not scene_data:
        return None
    return scene_data.get(role_key)


