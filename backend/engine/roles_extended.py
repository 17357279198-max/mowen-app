def get_school_list() -> list:
    """
    获取所有学派列表（去重），按角色数量降序排列。
    返回格式：[{"school": "儒家", "count": 4}, ...]
    """
    school_count = {}
    for role in ROLES.values():
        school = role["school"]
        school_count[school] = school_count.get(school, 0) + 1
    result = [{"school": s, "count": c} for s, c in school_count.items()]
    result.sort(key=lambda x: x["count"], reverse=True)
    return result


def get_era_list() -> list:
    """
    获取所有朝代列表（去重），按时间顺序排列。
    返回格式：[{"era": "春秋", "count": 4}, ...]
    """
    era_order = ["西周", "春秋", "战国", "西汉", "东晋", "三国", "北宋", "明朝", "清朝"]
    era_count = {}
    for role in ROLES.values():
        era = role["era"]
        era_count[era] = era_count.get(era, 0) + 1

    result = []
    for era in era_order:
        if era in era_count:
            result.append({"era": era, "count": era_count[era]})
    # 追加不在预设顺序中的朝代
    for era, count in era_count.items():
        if era not in era_order:
            result.append({"era": era, "count": count})
    return result
